CREATE TABLE IF NOT EXISTS staff (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  fname VARCHAR(30) NOT NULL,
  mname VARCHAR(30) NULL,
  lname VARCHAR(30) NOT NULL,
  local_address TEXT NOT NULL,
  permanent_address TEXT NOT NULL,
  dob DATE NOT NULL,
  doj DATE NOT NULL,
  religion_id BIGINT NOT NULL,
  castecategory_id BIGINT NOT NULL,
  gender VARCHAR(10) NOT NULL,
  date_of_increment DATE NULL,
  date_of_superanuation DATE NOT NULL,
  date_of_confirmation DATE NULL,
  bloodgroup VARCHAR(5) NULL,
  pan_card VARCHAR(20) NULL,
  adhar_card VARCHAR(20) NULL,
  contactno BIGINT NULL,
  aicte_id VARCHAR(30) NULL,
  vtu_id VARCHAR(30) NULL,
  esi_no VARCHAR(30) NULL,
  un_no VARCHAR(30) NULL,
  emergency_no BIGINT NULL,
  emergency_name VARCHAR(30) NULL,
  employeecode INTEGER NOT NULL DEFAULT 0,
  profile_photo VARCHAR(255) NULL,
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL,
  pf VARCHAR(100) NULL,
  CONSTRAINT staff_gender_check CHECK (gender IN ('female', 'male', 'others')),
  CONSTRAINT staff_user_fk FOREIGN KEY (user_id) REFERENCES users(id),
  CONSTRAINT staff_religion_fk FOREIGN KEY (religion_id) REFERENCES religions(id),
  CONSTRAINT staff_caste_fk FOREIGN KEY (castecategory_id) REFERENCES castecategories(id)
);