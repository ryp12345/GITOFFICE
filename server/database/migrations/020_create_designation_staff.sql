CREATE TABLE IF NOT EXISTS designation_staff (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  designation_id BIGINT NOT NULL,
  staff_id BIGINT NOT NULL,
  dept_id BIGINT DEFAULT NULL,
  start_date DATE NOT NULL,
  end_date DATE DEFAULT NULL,
  allowance_status VARCHAR(20) DEFAULT NULL,
  reason VARCHAR(255) DEFAULT NULL,
  gcr VARCHAR(255) DEFAULT NULL,
  gcr_close VARCHAR(255) DEFAULT NULL,
  status VARCHAR(20) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL,
  CONSTRAINT designation_staff_allowance_check CHECK (allowance_status IN ('Allowance','Grading','Both','')),
  CONSTRAINT designation_staff_status_check CHECK (status IN ('active','inactive')),
  CONSTRAINT designation_staff_designation_fk FOREIGN KEY (designation_id) REFERENCES designations(id) ON DELETE RESTRICT,
  CONSTRAINT designation_staff_staff_fk FOREIGN KEY (staff_id) REFERENCES staff(id) ON DELETE RESTRICT,
  CONSTRAINT designation_staff_department_fk FOREIGN KEY (dept_id) REFERENCES departments(id) ON DELETE RESTRICT
);
