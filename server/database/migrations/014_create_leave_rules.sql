CREATE TABLE IF NOT EXISTS leave_rules (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  leave_id BIGINT NOT NULL,
  carry_forwardable VARCHAR(3) NOT NULL DEFAULT 'No',
  cf_gcr VARCHAR(255) DEFAULT NULL,
  cf_wef DATE NOT NULL,
  cf_closing_date DATE DEFAULT NULL,
  cf_closing_gcr VARCHAR(255) DEFAULT NULL,
  max_cf INTEGER NOT NULL DEFAULT 0,
  entitlement_post_max_cf INTEGER DEFAULT NULL,
  encashable VARCHAR(3) NOT NULL DEFAULT 'No',
  enc_gcr VARCHAR(255) DEFAULT NULL,
  enc_wef DATE NOT NULL,
  enc_closing_date DATE DEFAULT NULL,
  enc_closing_gcr VARCHAR(255) DEFAULT NULL,
  max_enc INTEGER NOT NULL DEFAULT 0,
  gap VARCHAR(3) NOT NULL DEFAULT 'No',
  gap_gcr VARCHAR(255) DEFAULT NULL,
  gap_wef DATE DEFAULT NULL,
  gap_closing_date DATE DEFAULT NULL,
  gap_closing_gcr VARCHAR(255) DEFAULT NULL,
  min_gap INTEGER NOT NULL DEFAULT 0,
  max_time_allowed INTEGER DEFAULT NULL,
  period VARCHAR(100) DEFAULT NULL,
  prior_intimation_days INTEGER DEFAULT 0,
  status VARCHAR(8) NOT NULL DEFAULT 'active',
  created_at TIMESTAMP NULL,
  updated_at TIMESTAMP NULL
);
